public class ByteOperation{
	public static void main(String[] args){
	    int a = 32;
		int b = 12;
		int x = a & b;
		int y = a | b;
		int z = a ^ b;
		System.out.println("输出的结果是:"+a + "&" + b + "=" + x);
		System.out.println("输出的结果是:"+a + "|" + b + "=" + y);
		System.out.println("输出的结果是:"+a + "^" + b + "=" + z);
	
		//位运算
	}


}