class MyNumber{
	private int n1;
	private int n2;

	public MyNumber(int n1,int n2){
		this.n1 = n1;
		this.n2 = n2;
		
	
	}

	public int add(int n1,int n2){
		System.out.println("相加之后的结果是：" + (n1 + n2));
		return n1 + n2;
	}
	public int sub(int n1,int n2){
		System.out.println("相减之后的结果是：" + (n1 - n2));
		return n1 - n2;
		
	
	}
	public int mul(int n1,int n2){
		System.out.println("相乘之后的结果是：" + (n1 * n2));
		return n1 * n2;
		
	
	}
	public int div(int n1,int n2){
		System.out.println("相除之后的结果是：" + (n1 / n2));
		return n1 / n2;
		
	}

}
class MyNumberDemo{

	public static void main(String[] args){
	    MyNumber mn = new MyNumber(90,30);

		mn.add(90,30);
		mn.sub(90,30);
		mn.mul(90,30);
		mn.div(90,30);

	}
}
